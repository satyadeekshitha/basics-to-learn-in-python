def add(a,b):
    return a+b
def sub(f,g):
    return f-g
def mul(a,b):
    return a*b
def div(x,y):
    return x/y
add = add(5,4)
sub = sub(14,15) 
mul = mul(9,14)
div = div(45,5)
print("addition",add) # prints 9
print("subraction",sub) # prints -1
print("multiplication",mul) # prints 126
print("division",div) # prints 9.0
