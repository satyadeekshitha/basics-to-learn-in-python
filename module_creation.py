def add(a,b):
    return a+b
def sub(f,g):
    return f-g
def mul(a,b):
    return a*b
def div(x,y):
    return x/y
add = add(5,4)
sub = sub(14,15) 
mul = mul(9,14)
div = div(45,5)
print("addition",add)
print("subraction",sub)
print("multiplication",mul)
