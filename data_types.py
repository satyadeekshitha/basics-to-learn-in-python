# There are two types of data types: primitive and non-primitive data types.
# Primitive data types include:
# int:
a = 10
print("a:", a)
# float:
b = 3.4
print("b:", b)
# string:
c = "Hello Satya"
print("c:", c)
# boolean:
d = 5
e = 6
if d < e:
    print("d is less than e:", True)
else:
    print("d is greater than e:", False)
# Non-primitive data types include:
# list:
na_list = [25,10,11,20,7]
print("na_list:", na_list)
# tuple:
my_tuple = (25,10,11,200,7)
print("my_tuple:", my_tuple)
# dictionary:
my_dic = {"name": "Satya", "age": 18, "state": "andhra pradesh"}
print("my_dic:", my_dic)
# set:
my_set = {25,10,11,200,7}
print("my_set:", my_set)


